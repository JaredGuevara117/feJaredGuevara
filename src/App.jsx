import { useState, useEffect, useRef } from 'react'
import reactLogo from './assets/react.svg'
import viteLogo from '/vite.svg'
import './App.css'

function App() {
  console.log('üöÄ App cargada - Lista de Tareas v1.2.0')
  const [isOnline, setIsOnline] = useState(navigator.onLine)
  const [cachedData, setCachedData] = useState([])
  const [newItem, setNewItem] = useState('')

  // Detectar estado de conexi√≥n
  useEffect(() => {
    const handleOnline = () => setIsOnline(true)
    const handleOffline = () => setIsOnline(false)

    window.addEventListener('online', handleOnline)
    window.addEventListener('offline', handleOffline)

    return () => {
      window.removeEventListener('online', handleOnline)
      window.removeEventListener('offline', handleOffline)
    }
  }, [])

  // Cargar datos desde localStorage (simulando cache)
  useEffect(() => {
    const savedData = localStorage.getItem('offlineData')
    if (savedData) {
      setCachedData(JSON.parse(savedData))
    }
  }, [])

  // Guardar datos en localStorage
  const saveToCache = (data) => {
    const updatedData = [...cachedData, data]
    setCachedData(updatedData)
    localStorage.setItem('offlineData', JSON.stringify(updatedData))
  }

  // Agregar nuevo elemento
  const addItem = () => {
    if (newItem.trim()) {
      const item = {
        id: Date.now(),
        text: newItem,
        timestamp: new Date().toLocaleString(),
        synced: isOnline, // Si est√°s online, se marca como sincronizado
        completed: false
      }
      saveToCache(item)
      setNewItem('')
    }
  }

  // Marcar tarea como completada
  const toggleTask = (taskId) => {
    const updatedData = cachedData.map(item => {
      if (item.id === taskId) {
        return { ...item, completed: !item.completed }
      }
      return item
    })
    
    setCachedData(updatedData)
    localStorage.setItem('offlineData', JSON.stringify(updatedData))
  }

  // Simular sincronizaci√≥n cuando vuelve la conexi√≥n
  const syncData = async () => {
    if (isOnline) {
      // Simular env√≠o de datos al servidor
      console.log('Sincronizando datos:', cachedData)
      
      // Marcar todos los datos como sincronizados
      const syncedData = cachedData.map(item => ({
        ...item,
        synced: true
      }))
      
      setCachedData(syncedData)
      localStorage.setItem('offlineData', JSON.stringify(syncedData))
      
      alert('Datos sincronizados con el servidor!')
    }
  }

  return (
    <>
      <div>
        <a href="https://vite.dev" target="_blank">
          <img src={viteLogo} className="logo" alt="Vite logo" />
        </a>
        <a href="https://react.dev" target="_blank">
          <img src={reactLogo} className="logo react" alt="React logo" />
        </a>
      </div>
      
      <h1>PWA Demo - Funcionalidad Offline</h1>
      
      {/* Indicador de estado de conexi√≥n */}
      <div className="connection-status">
        <div className={`status-indicator ${isOnline ? 'online' : 'offline'}`}>
          {isOnline ? 'üü¢ En l√≠nea' : 'üî¥ Sin conexi√≥n'}
        </div>
        {!isOnline && (
          <p className="offline-message">
            Est√°s offline. Los datos se guardar√°n localmente y se sincronizar√°n cuando vuelva la conexi√≥n.
          </p>
        )}
      </div>

      <div className="card">
        <h3>üìù Lista de Tareas Offline</h3>
        <p>
          Agrega tareas, m√°rcalas como completadas y todo se guarda autom√°ticamente.
          Funciona perfectamente sin conexi√≥n a internet.
        </p>
      </div>

      {/* Funcionalidad offline demo */}
      <div className="offline-demo">
        <h2>Demo de Funcionalidad Offline</h2>
        
        <div className="add-item">
          <input
            type="text"
            value={newItem}
            onChange={(e) => setNewItem(e.target.value)}
            placeholder="Agregar elemento..."
            onKeyPress={(e) => e.key === 'Enter' && addItem()}
          />
          <button onClick={addItem}>Agregar</button>
        </div>

        {cachedData.length > 0 && (
          <div className="cached-items">
            <h3>Tareas guardadas {isOnline ? '(en l√≠nea)' : '(offline)'}</h3>
            <ul>
              {cachedData.map(item => (
                <li key={item.id} className={`${item.synced ? 'synced' : 'pending'} ${item.completed ? 'completed' : ''}`}>
                  <div className="task-content">
                    <input
                      type="checkbox"
                      checked={item.completed}
                      onChange={() => toggleTask(item.id)}
                      className="task-checkbox"
                    />
                    <span className={`task-text ${item.completed ? 'completed-text' : ''}`}>
                      {item.text}
                    </span>
                  </div>
                  <div className="task-meta">
                    <small>{item.timestamp}</small>
                    {!item.synced && <span className="sync-badge">Pendiente</span>}
                  </div>
                </li>
              ))}
            </ul>
          </div>
        )}

        {isOnline && cachedData.some(item => !item.synced) && (
          <button onClick={syncData} className="sync-button">
            Sincronizar datos pendientes
          </button>
        )}
      </div>

      <div className="pwa-info">
        <h3>Caracter√≠sticas PWA implementadas:</h3>
        <ul>
          <li>‚úÖ Cache de APP SHELL (rutas fijas)</li>
          <li>‚úÖ Cache din√°mico (contenido nuevo)</li>
          <li>‚úÖ Eliminaci√≥n autom√°tica de cache vieja</li>
          <li>‚úÖ Activaci√≥n autom√°tica del Service Worker</li>
          <li>‚úÖ Funcionalidad offline completa</li>
          <li>‚úÖ Sincronizaci√≥n en segundo plano</li>
        </ul>
      </div>
    </>
  )
}

export default App
